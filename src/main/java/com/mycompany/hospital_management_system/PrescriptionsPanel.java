    private void btnSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSAVEActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn=DatabaseConnection.getConnection();
            
        int rid=Integer.parseInt(txtRID.getText());
        PreparedStatement checkRecord=conn.prepareStatement("SELECT record_id FROM medical_records WHERE record_id=?");
        checkRecord.setInt(1, rid);
        ResultSet rsr=checkRecord.executeQuery();
        
        if(rsr.next()){
        JOptionPane.showMessageDialog(null,"Record id"+rid+"doesn't exist");
        }else{
            Connection cons=DatabaseConnection.getConnection();
        
        String sql="INSERT INTO prescriptions(record_id,medicine_name,dosage,duration)values(?,?,?,?)";
        
        PreparedStatement pst=cons.prepareStatement(sql);
        pst.setInt(1, rid);
        
        pst.setString(2,txtMEDNAME.getText());
        pst.setString(3,txtDOSAGE.getText());
        
        int duration=(int)spinnerDURATION.getValue();
        pst.setInt(4,duration);
        
        
        
        pst.executeQuery();
        JOptionPane.showMessageDialog(null, "BILL SAVED");
        }
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,"Error"+e.getMessage());
        }
    }//GEN-LAST:event_btnSAVEActionPerformed

