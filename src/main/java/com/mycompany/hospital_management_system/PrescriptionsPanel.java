    private void btnnUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnUPDATEActionPerformed
        // TODO add your handling code here:
        try{
            if(prescriptionID==-1){
            JOptionPane.showMessageDialog(null,"Choose a valid Prescription id");}
            
            int rid=Integer.parseInt(txtRID.getText());
            Connection conn=DatabaseConnection.getConnection();
            
            String sql="UPDATE prescriptions SET record_id=?,medicine_name=?,dosage=?,duration=? WHERE prescription_id=?";
          PreparedStatement pst=conn.prepareStatement(sql);
          pst.setInt(1, rid);
         
          pst.setString(2,txtMEDNAME.getText());
          pst.setString(3,txtDOSAGE.getText());
          
          int duration=(int)spinnerDURATION.getValue();
        pst.setInt(4,duration);
        
        pst.setInt(5, prescriptionID);
        pst.executeQuery();
          
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,"Error" + e.getMessage());}
    }//GEN-LAST:event_btnnUPDATEActionPerformed

