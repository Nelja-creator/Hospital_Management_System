    private void btnDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDELETEActionPerformed
        // TODO add your handling code here:
        if(prescriptionID==-1){
          JOptionPane.showMessageDialog(null,"Please select a valid row");
          return;
        }
        int dialogResult=JOptionPane.showConfirmDialog(null,"Are you sure you want to delete this patient data","waning",YES_NO_OPTION);
        if(dialogResult==JOptionPane.YES_OPTION){
        try{
            
        Connection conn=DatabaseConnection.getConnection();
        
        String sql="DELETE FROM prescriptions WHERE prescription_id=?";
        PreparedStatement pst=conn.prepareStatement(sql);
        
        pst.setInt(1, prescriptionID);
        pst.executeQuery();
        
        JOptionPane.showMessageDialog(null,"PRESCRIPTION DELETED SUCCESSFULLY");
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error"+e.getMessage());
        }}
    }//GEN-LAST:event_btnDELETEActionPerformed

