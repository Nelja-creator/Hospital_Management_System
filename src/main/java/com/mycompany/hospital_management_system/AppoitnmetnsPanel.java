    private void btnUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUPDATEActionPerformed
        // TODO add your handling code here:
        if(appId==-1){
        JOptionPane.showMessageDialog(null,"Please choose a valid");
        }
        try{
            int Patientid=Integer.parseInt(txtPID.getText());
            int Doctorid=Integer.parseInt(txtDID.getText());
            Connection conn=DatabaseConnection.getConnection();
            String sql="UPDATE appointments SET patient_id=?,doctor_id=?,appointment_date=?,appointment_time=?,status=? WHERE appointment_id=?";
            
            PreparedStatement pst=conn.prepareStatement(sql);
            pst.setInt(1,Patientid);
            pst.setInt(2, Doctorid);
            
            java.util.Date sdate=(java.util.Date)spinnerDATE.getValue();
            pst.setDate(3, new java.sql.Date(sdate.getTime()));
            
            java.util.Date tdate=(java.util.Date)spinnerTIME.getValue();
            pst.setTime(4,new java.sql.Time(tdate.getTime()));
            pst.setString(5,comboSTATUS.getSelectedItem().toString());
            pst.setInt(6,appId);
            
            pst.executeQuery();
            JOptionPane.showMessageDialog(null,"APPOINTMENT DATA SAVED");
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,"Error"+e.getMessage());
        }
    }//GEN-LAST:event_btnUPDATEActionPerformed

