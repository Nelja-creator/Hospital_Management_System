    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row=jTable1.getSelectedRow();
        
        appId=Integer.parseInt(jTable1.getValueAt( row, 0).toString());
        txtPID.setText(jTable1.getValueAt(row, 1).toString());
        txtDID.setText(jTable1.getValueAt(row, 2).toString());
        //handle the jspinner
        try{
            String dateValue=jTable1.getValueAt(row, 3).toString();
            java.util.Date date=new java.text.SimpleDateFormat("yyyy-MM-dd").parse(dateValue);
            spinnerDATE.setValue(date);
        }catch(Exception e){
        spinnerDATE.setValue(new java.util.Date());
        }
        //handle time spinner
        try{
            String timeValue=jTable1.getValueAt(row,4).toString();
            java.util.Date time=new java.text.SimpleDateFormat("HH:mm").parse(timeValue);
            spinnerTIME.setValue(time);
        }catch(Exception e){
        spinnerTIME.setValue(new java.util.Date());
        }
        //handle combo box
        String status=jTable1.getValueAt(row, 5).toString();
        comboSTATUS.setSelectedItem(status);
        
    }//GEN-LAST:event_jTable1MouseClicked


