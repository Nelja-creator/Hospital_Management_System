    private void btnSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSAVEActionPerformed
        // TODO add your handling code here:
        //code to validate patien id and doctors id exist
        try{
      Connection conn=DatabaseConnection.getConnection();
      int pid=Integer.parseInt(txtPID.getText());
      int did=Integer.parseInt(txtDID.getText());
      
      PreparedStatement checkPatient=conn.prepareStatement("SELECT patient_id FROM patients WHERE patient_id=?");
      checkPatient.setInt(1, pid);
      ResultSet rsP=checkPatient.executeQuery();
      
      PreparedStatement checkDoctor=conn.prepareStatement("SELECT doctor_id FROM doctors WHERE doctor_id=? ");
      checkDoctor.setInt(2, did);
      ResultSet rsD=checkDoctor.executeQuery();
      
      if(rsP.next()){
      JOptionPane.showMessageDialog(null,"Pateint id:"+ pid +" does not exist");
          }else if(rsD.next()){
       JOptionPane.showMessageDialog(null,"Doctor id:" + did + "does not exist");
          }else{
              
          Connection cons=DatabaseConnection.getConnection();
          
          
          String sql="INSERT INTO appointments (patient_id,doctor_id,appointment_date,appointment_time,status)VALUES(?,?,?,?,?)";
          PreparedStatement pst=cons.prepareStatement(sql);
          pst.setInt(1, pid);
          pst.setInt(2, did);
          java.util.Date appdate=(java.util.Date)spinnerDATE.getValue();
          pst.setDate(3,new java.sql.Date(appdate.getTime()) );
          java.util.Date apptime=(java.util.Date)spinnerTIME.getValue();
          pst.setTime(5,new java.sql.Time(apptime.getTime()));
          pst.setString(6,comboSTATUS.getSelectedItem().toString());
          
          pst.executeQuery();
          JOptionPane.showMessageDialog(null,"APPOINTMENT SAVED");
          
          }
              }catch(Exception e) {
              JOptionPane.showMessageDialog(null, "");
              }
    
              
    }//GEN-LAST:event_btnSAVEActionPerformed

