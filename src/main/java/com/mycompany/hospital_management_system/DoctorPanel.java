    private void btnDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDELETEActionPerformed
        // TODO add your handling code here:
        if(docID==-1){
           JOptionPane.showMessageDialog(null,"please select a valid row");
           return;
        }
        int dialog=JOptionPane.showConfirmDialog(null,"Are you sure you want to delete?","warning",YES_NO_OPTION );
        if(dialog==JOptionPane.YES_OPTION){
        try{
            Connection conn=DatabaseConnection.getConnection();
            
            String sql="DELETE FROM doctors WHERE doctor_id=?";
            PreparedStatement pst=conn.prepareStatement(sql);
            pst.setInt(1, docID);
            pst.executeQuery();
            
            JOptionPane.showMessageDialog(null,"DOCTOR DATA DELETED");
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,"Error"+e.getMessage());}
        }  
    }//GEN-LAST:event_btnDELETEActionPerformed

