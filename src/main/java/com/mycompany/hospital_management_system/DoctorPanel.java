    private void btnUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUPDATEActionPerformed
        // TODO add your handling code here:
        if(docID==-1){
        JOptionPane.showMessageDialog(null,"Please choose a valid row");
        }
        try{
            Connection conn=DatabaseConnection.getConnection();
            
            String sql="UPDATE doctors SET user_id=?,specialisation=? WHERE doctor_id=?";
            PreparedStatement pst=conn.prepareStatement(sql);
            
            pst.setString(1,txtUSER.getText().trim());
            pst.setString(2,comboSPECIAL.getSelectedItem().toString());
            pst.setInt(3, docID);
            
            pst.executeQuery();
            JOptionPane.showMessageDialog(null,"DOCTOR DATA SAVED SUCCESSFULLY");
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Error"+e.getMessage());}
    }//GEN-LAST:event_btnUPDATEActionPerformed

