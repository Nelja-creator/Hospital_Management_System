    private void btnUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUPDATEActionPerformed
        // TODO add your handling code here:
        if(idToWorkWith==-1){
        JOptionPane.showMessageDialog(null,"Please choose a valid row");
        }
        try{
            Connection conn=DatabaseConnection.getConnection();
            
            String sql="UPDATE patients SET first_name=?,last_name=?,dob=?,gender=?,address=?,phone=?,email=?";
            PreparedStatement pst=conn.prepareStatement(sql);
            
            pst.setString(1,txtFNAME.getText().trim());
            pst.setString(2,txtLNAME.getText().trim());
            java.util.Date sdate=(java.util.Date)spinnerDATE.getValue();
            pst.setDate(3, new java.sql.Date(sdate.getTime()));
            
            pst.setString(4,comboG.getSelectedItem().toString());
            pst.setString(5,txtADDRESS.getText().trim());
            pst.setString(6,txtPHONE.getText().trim());
            pst.setString(7,txtEMAIL.getText().trim());
            
            pst.executeQuery();
            JOptionPane.showMessageDialog(null,"PATIENT DATA SAVED");
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,"Error"+e.getMessage());}
    }//GEN-LAST:event_btnUPDATEActionPerformed

