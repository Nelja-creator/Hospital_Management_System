    private void btnDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDELETEActionPerformed
        // TODO add your handling code here:
        //checks if row is actually selected
        if(idToWorkWith==-1){
          JOptionPane.showMessageDialog(null,"Please select a valid row");
          return;
        }
        int dialogResult=JOptionPane.showConfirmDialog(null,"Are you sure you want to delete this patient data","waning",YES_NO_OPTION);
        if(dialogResult==JOptionPane.YES_OPTION){
        try{
            Connection conn=DatabaseConnection.getConnection();
            
            String sql="DELETE FROM patients where patient_id=?";
            PreparedStatement pst=conn.prepareStatement(sql);
            
            pst.setInt(1, idToWorkWith);
            pst.executeQuery();
            
            JOptionPane.showMessageDialog(null, "PATIENT DATA DEETED");
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Error"+e.getMessage());}
        }
    }//GEN-LAST:event_btnDELETEActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row=jTable1.getSelectedRow();
        
        idToWorkWith=Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        txtFNAME.setText(jTable1.getValueAt(row, 1).toString());
        txtLNAME.setText(jTable1.getValueAt(row, 2).toString());
        //handle the jspinner
        try{
            String dateValue=jTable1.getValueAt(row, 3).toString();
            java.util.Date date=new java.text.SimpleDateFormat("yyyy-MM-dd").parse(dateValue);
            spinnerDATE.setValue(date);
        }catch(Exception e){
        spinnerDATE.setValue(new java.util.Date());
        }
        //handle combo box
        String gender=jTable1.getValueAt(row, 4).toString();
        comboG.setSelectedItem(gender);
        txtADDRESS.setText(jTable1.getValueAt(row, 5).toString());
        txtPHONE.setText(jTable1.getValueAt(row, 6).toString());
        txtEMAIL.setText(jTable1.getValueAt(row, 7).toString());
        
    }//GEN-LAST:event_jTable1MouseClicked


