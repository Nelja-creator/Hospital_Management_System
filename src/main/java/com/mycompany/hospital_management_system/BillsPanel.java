    private void btnSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSAVEActionPerformed
        // TODO add your handling code here:
        try{
            Connection cons=DatabaseConnection.getConnection();
            
            int pid=Integer.parseInt(txtPID.getText());
            PreparedStatement checkPatient=cons.prepareStatement("SELECT patient_id FROM patients WHERE patient_id=?");
            checkPatient.setInt(1, pid);
            ResultSet rsp=checkPatient.executeQuery();
            
            if(rsp.next()){
            JOptionPane.showMessageDialog(null,"Patient_id"+pid+"doesn't exist");
            }else{
            Connection conn=DatabaseConnection.getConnection();
        
        String sql="INSERT INTO medical_records(pateint_id,total_amount,payment_status,bill_date)values(?,?,?,?)";
        
        PreparedStatement pst=conn.prepareStatement(sql);
        pst.setInt(1, pid);
        
        pst.setString(2,txtTOTAL.getText());
        pst.setString(3,comboSTATUS.getSelectedItem().toString());
        
        java.util.Date recordDate=(java.util.Date)spinnerDATE.getValue();
        pst.setDate(4, new java.sql.Date(recordDate.getTime()));
        
        pst.executeQuery();
        JOptionPane.showMessageDialog(null, "BILL SAVED");
            }
            
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,"Error"+e.getLocalizedMessage());
        }
    }//GEN-LAST:event_btnSAVEActionPerformed

