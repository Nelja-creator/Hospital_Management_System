    private void btnDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDELETEActionPerformed
        // TODO add your handling code here:
        try{
        if(billID==-1){
        JOptionPane.showMessageDialog(null,"Choose a valid bill id");
        }
        
        Connection conn=DatabaseConnection.getConnection();
        
        String sql="DELETE FROM bills WHERE bill_id=?";
        PreparedStatement pst=conn.prepareStatement(sql);
        
        pst.setInt(1, billID);
        pst.executeQuery();
        
        JOptionPane.showMessageDialog(null,"BILL DELETED SUCCESSFULLY");
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Error"+e.getMessage());
        }
    }//GEN-LAST:event_btnDELETEActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row=jTable1.getSelectedRow();
        
        billID=Integer.parseInt(jTable1.getValueAt(row,0).toString());
        txtPID.setText(jTable1.getValueAt(row,1).toString());
        
        txtTOTAL.setText(jTable1.getValueAt(row,2).toString());
        comboSTATUS.setSelectedItem(jTable1.getValueAt(row,4).toString());
        
        try{
            String dateValue=jTable1.getValueAt(row, 3).toString();
            java.util.Date date=new java.text.SimpleDateFormat("yyyy-MM-dd").parse(dateValue);
            spinnerDATE.setValue(date);
        }catch(Exception e){
        spinnerDATE.setValue(new java.util.Date());
        }
    }//GEN-LAST:event_jTable1MouseClicked


