    private void btnUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUPDATEActionPerformed
        // TODO add your handling code here:
        try{
          if(billID==-1){
        JOptionPane.showMessageDialog(null,"Choose a valid record id");
        }
          int pid=Integer.parseInt(txtPID.getText());
          
          
          Connection conn=DatabaseConnection.getConnection();
          
          String sql="UPDATE bills SET patient_id=?,total_amount=?,payment_status=?,bill_date=? WHERE bill_id=?";
          PreparedStatement pst=conn.prepareStatement(sql);
          pst.setInt(1, pid);
         
          pst.setString(2,txtTOTAL.getText());
          pst.setString(3,comboSTATUS.getSelectedItem().toString());
          
          java.util.Date recordd=(java.util.Date)spinnerDATE.getValue();
          pst.setDate(4,new java.sql.Date(recordd.getTime()));
          
          pst.setInt(5, billID);
          pst.executeQuery();
          JOptionPane.showMessageDialog(null,"RECORD UPDATED");
          
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Error"+e.getMessage());
        }
    }//GEN-LAST:event_btnUPDATEActionPerformed

